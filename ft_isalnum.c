#include "libft.h"

int	ft_isalpha(int c)
{
	if (!((c <= 122 && c >= 97)
			|| (c >= 65 && c <= 90)))
	{
		return (0);
	}
	return (1);
}

int	ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
	{
		return (1);
	}
	return (0);
}

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
	{
		return (1);
	}
	return (0);
}
